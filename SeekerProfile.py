# -*-coding:utf-8-*-


class SeekerProfile:

    def __init__(self, seeker_id, name, gender, mobile, email, education_level, work_year, skill_set):
        self.seeker_id = seeker_id
        self.name = name
        self.gender = gender
        self.mobile = mobile
        self.email = email
        self.education_level = education_level
        self.work_year = work_year
        self.skill_set = skill_set

